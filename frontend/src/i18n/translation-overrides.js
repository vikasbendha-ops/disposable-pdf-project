function deepMergeTranslationOverrides(target, source) {
  for (const [key, value] of Object.entries(source)) {
    if (
      value &&
      typeof value === 'object' &&
      !Array.isArray(value) &&
      target[key] &&
      typeof target[key] === 'object' &&
      !Array.isArray(target[key])
    ) {
      deepMergeTranslationOverrides(target[key], value);
      continue;
    }
    target[key] = value;
  }
}

